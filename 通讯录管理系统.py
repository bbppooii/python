class 通讯录:
    def __init__(self):
        self.联系人 = {}

    def 添加联系人(self, 姓名, 电话, 邮箱):
        self.联系人[姓名] = {"电话": 电话, "邮箱": 邮箱}
        print(f"已添加联系人：{姓名}")

    def 删除联系人(self, 姓名):
        if 姓名 in self.联系人:
            del self.联系人[姓名]
            print(f"已删除联系人：{姓名}")
        else:
            print("联系人不存在")

    def 修改联系人(self, 姓名, 电话, 邮箱):
        if 姓名 in self.联系人:
            self.联系人[姓名] = {"电话": 电话, "邮箱": 邮箱}
            print(f"已修改联系人 {姓名} 的信息")
        else:
            print("未找到联系人")

    def 查找联系人(self, 姓名):
        if 姓名 in self.联系人:
            print(f"姓名: {姓名}, 电话: {self.联系人[姓名]['电话']}, 邮箱: {self.联系人[姓名]['邮箱']}")
        else:
            print("未找到联系人")

    def 显示所有联系人(self):
        if self.联系人:
            print("所有联系人：")
            for 姓名, 信息 in self.联系人.items():
                print(f"姓名: {姓名}, 电话: {信息['电话']}, 邮箱: {信息['邮箱']}")
        else:
            print("通讯录为空")


通讯录实例 = 通讯录()

while True:
    选择 = input("\n1. 添加 2. 删除 3. 修改 4. 查找 5. 显示所有 6. 退出\n请选择操作：")

    if 选择 == "1":
        姓名 = input("姓名：")
        电话 = input("电话：")
        邮箱 = input("邮箱：")
        通讯录实例.添加联系人(姓名, 电话, 邮箱)

    elif 选择 == "2":
        姓名 = input("请输入要删除的姓名：")
        通讯录实例.删除联系人(姓名)

    elif 选择 == "3":
        姓名 = input("请输入要修改的姓名：")
        电话 = input("新电话：")
        邮箱 = input("新邮箱：")
        通讯录实例.修改联系人(姓名, 电话, 邮箱)

    elif 选择 == "4":
        姓名 = input("请输入要查找的姓名：")
        通讯录实例.查找联系人(姓名)

    elif 选择 == "5":
        通讯录实例.显示所有联系人()

    elif 选择 == "6":
        print("退出通讯录")
        break

    else:
        print("无效输入")
